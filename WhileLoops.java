/**
 * A program that demonstrates while loops.
 */

public class WhileLoops {

    public static void main(String[] args) {

        // while loop
        int i = 0;
        while (i < 10) {
            System.out.println(i);
            i++;
        }

        // do/while loop
        int j = 0;
        do {
            System.out.println(j);
            j++;
        }
        while (j < 10);
    }
}
